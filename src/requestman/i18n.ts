function getPrimaryBrowserLanguage() {
  if (typeof navigator === 'undefined') return 'en';
  if (typeof navigator.language === 'string' && navigator.language.trim()) {
    return navigator.language.toLowerCase();
  }
  if (Array.isArray(navigator.languages) && navigator.languages.length > 0) {
    const first = navigator.languages.find((value) => typeof value === 'string' && value.trim().length > 0);
    if (first) return first.toLowerCase();
  }
  return 'en';
}

export function getLocale(): Locale {
  const language = getPrimaryBrowserLanguage();

  if (HANT_LANGUAGE_PATTERNS.some((pattern) => language.startsWith(pattern))) {
    return 'zh-Hant';
  }

  if (language.startsWith('zh')) {
    return 'zh-Hans';
  }

  return 'en';
}

function replacePhrase(input: string): string {
  return HANT_PHRASE_MAP.reduce(
    (output, [source, target]) => output.split(source).join(target),
    input,
  );
}

export function toTraditionalChinese(input: string): string {
  const withPhrases = replacePhrase(input);
  return Array.from(withPhrases, (char) => HANT_CHAR_MAP[char] ?? char).join('');
}

export const locale: Locale = getLocale();

export const htmlLang = locale === 'zh-Hant' ? 'zh-Hant' : locale === 'zh-Hans' ? 'zh-Hans' : 'en';

export function applyLocaleToDocument(title?: string) {
  if (typeof document === 'undefined') return;
  document.documentElement.lang = htmlLang;
  if (title) document.title = title;
}

export const t = (zhHans: string, en: string): string => {
  if (locale === 'en') return en;
  return locale === 'zh-Hant' ? toTraditionalChinese(zhHans) : zhHans;
};
